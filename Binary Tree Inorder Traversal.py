"""Given the root of a binary tree, return the inorder traversal of its nodes values.
Example:

Input: root = []
Output: []"""

class Solution:
    def inorderTraversal(self, root: Optional[TreeNode]) -> List[int]:
        st = []
        res = []

        while root or st:
            while root:
                st.append(root)
                root = root.left
            
            root = st.pop()
            res.append(root.val)

            root = root.right
        
        return res 

